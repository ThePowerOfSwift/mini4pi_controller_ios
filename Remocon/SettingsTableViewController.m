- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ipaddress" forIndexPath:indexPath];
    
    UITextField* textIpAddress = (UITextField*)[cell viewWithTag:UITAG_IP_ADDRESS];
    NSString* ipaddress = [MyDefaults loadIpAddress];
    if(!ipaddress || [ipaddress isEqualToString:@""]){
        textIpAddress.text = @"localhost";
    }
    else{
        textIpAddress.text = ipaddress;
    }
    
    textIpAddress.delegate = self;
    
    return cell;
}


